/**
 * 创建 SVG 图标元素
 */
export function createSvgIcon(
  iconName: keyof typeof SVG_ICONS,
  options: SvgIconOptions = {}
): HTMLElement {
  const { size = 16, className = '', color = 'currentColor' } = options;
  
  const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
  svg.setAttribute('viewBox', '0 0 512 512');
  svg.setAttribute('width', size.toString());
  svg.setAttribute('height', size.toString());
  svg.setAttribute('fill', color);
  
  if (className) {
    svg.className = className;
  }
  
  const path = document.createElementNS('http://www.w3.org/2000/svg', 'path');
  path.setAttribute('d', SVG_ICONS[iconName]);
  path.setAttribute('fill', color);
  
  svg.appendChild(path);
  
  return svg;
}

/**
 * 创建 SVG 图标字符串
 */
export function createSvgIconString(
  iconName: keyof typeof SVG_ICONS,
  options: SvgIconOptions = {}
): string {
  const { size = 16, className = '', color = 'currentColor' } = options;
  
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512" width="${size}" height="${size}" fill="${color}" ${className ? `class="${className}"` : ''}>
    <path d="${SVG_ICONS[iconName]}" fill="${color}"/>
  </svg>`;
}

/**
 * 获取 SVG 图标的 data URI
 */
export function getSvgIconDataUri(
  iconName: keyof typeof SVG_ICONS,
  color: string = 'currentColor'
): string {
  const svgString = `<svg xmlns='http://www.w3.org/2000/svg' viewBox='0 0 512 512'><path fill='${color}' d='${SVG_ICONS[iconName]}'/></svg>`;
  return `data:image/svg+xml,${encodeURIComponent(svgString)}`;
}

