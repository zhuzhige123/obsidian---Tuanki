/**
 * 根据 FontAwesome 类名创建 SVG 图标
 */
export function createIconFromFaClass(
  faClassName: string,
  options: SvgIconOptions = {}
): HTMLElement | null {
  const iconName = ICON_MAPPING[faClassName as keyof typeof ICON_MAPPING];
  if (!iconName) {
    console.warn(`未找到图标映射: ${faClassName}`);
    return null;
  }
  
  return createSvgIcon(iconName, options);
}
