            }
          } else {
            memoryCurveData = [];
          }

          if (Array.isArray(difficultyDist)) {
            difficultyData = sampleData(difficultyDist, 50);
          } else {
            difficultyData = [];
          }

          if (Array.isArray(parameterAnalysis)) {
            parameterImpact = sampleData(parameterAnalysis, 30);
          } else {
            parameterImpact = [];
